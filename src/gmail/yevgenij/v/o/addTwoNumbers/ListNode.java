package gmail.yevgenij.v.o.addTwoNumbers;

public class ListNode {
    private int num;
    private ListNode next;
    ListNode(int num){
        this.num = num;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public ListNode getNext() {
        return next;
    }

    public void setNext(ListNode next) {
        this.next = next;
    }
}
